public class Conventions {
    public static void main(String[] args) {
        // hay tres formas de nombrar a una variable
        // comenzar con '_', '$' o con una letra, nunca comenzar con un número
        // usar camelCase
        int cellPhone =985705994;
        String $countryName = "Perú";
        String _backgroundColor = "Black";
        // Las constantes van en mayusculas y con '_'
        double NUMBER_PHI = 3.1416;


        // PODEMOS RESUMIRLO ASÍ
        // LowerCamelCase para métodos y variables
        boolean isAvailable = true;
        // UpperCamelCase para Clases, interfaces
        String Connection;
        // Snake Case para constantes y en mayúsucla
        int MAX_SIZE = 30;


    }
}
